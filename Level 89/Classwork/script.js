// 1. შექმენით while loop, სადაც გამოიტანთ 1-იდან 10-მდე რიცხვებს

let i = 1;

while (i <= 10) {
    console.log(i);
    i++;
}

// 2. შექმენით while loop, სადაც გამოიტანთ 'hello'-ს 5-ჯერ.

let wloop = 0;

while (wloop < 5) {
    console.log("hello");
    wloop++;
}

// 3. შექმენით for loop, სადაც გამოიტანთ 2-იდან 6-მდე რიცხვებს.

for (let i = 2; i <= 6; i++) {
    console.log(i);
}

// 4. შექმენით for loop, სადაც გამოიტანთ 'hello'-ს 6-ჯერ.

for (let i = 0; i < 6; i++) {
    console.log("hello");
}













